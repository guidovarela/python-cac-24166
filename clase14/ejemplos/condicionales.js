// Ejemplo de estructura condicional (if-else)
/*
Utilizamos una estructura condicional if-else para verificar si la variable edad es mayor o igual a 18. Dependiendo del resultado de la condición, se imprimirá un mensaje diferente en la consola.
*/

let edad = 18;

if (edad >= 18) {
    console.log("Eres mayor de edad.");
} else {
    console.log("Eres menor de edad.");
}